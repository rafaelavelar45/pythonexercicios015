msg = input('Digite algo: ')
print(f'Você escreveu: {msg}')
print(f'O tipo primitivo de {msg} é {type(msg)}')
print(f'{msg} é um alfanumerico? {msg.isalnum()}')
print(f'{msg} é alfabetico? {msg.isalpha()}')
print(f'{msg} é maiusuculo? {msg.isupper()}')
print(f'{msg} é minusculo? {msg.islower()}')
print(f'{msg} é um digito? {msg.isdigit()}')
print(f'{msg} é um espaço? {msg.isspace()}')
print(f'{msg} etá capitalizado? {msg.istitle()}')
print('Analise Concluida!!')

















